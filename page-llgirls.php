        <h2>Um trabalho contínuo e em conjunto…</h2>

        <p>Embora tenhamos orgulho de tudo o que construímos até aqui em termos de inclusão feminina, no entanto, reconhecemos que ainda há muito a se fazer. As mulheres ainda representam 37,5% do total de membros do projeto — o que é uma porcentagem elevada se considerarmos a distribuição de gêneros nos cursos de TI do campus, mas insuficiente para nossos esforços visando atingir uma representatividade plena.</p>
        
        <p>É por isso que estamos lançando oficialmente o programa <strong>LLGirls</strong>, uma iniciativa já em vigor há alguns meses internamente no LearningLab que visa promover ainda mais a participação feminina no projeto.</p>

        <p>Jacilane, que atua como coordenadora do LearningLab e exerce o principal papel de liderança no projeto, destacou que a ideia do LLGirls surgiu naturalmente, a partir da observação do trabalho desempenhado por integrantes mulheres, bem como da escassez no que diz respeito ao ingresso feminino nos cursos de TI do campus da UFC de Russas.</p>


        <p>A <strong>inclusão feminina</strong> tem sido um dos grandes desafios enfrentados no mundo da Tecnologia da Informação (TI). Frequentemente associadas a profissões que são tradicionalmente vistas como “femininas”, muitas mulheres são desaconselhadas a seguir carreiras que exigem pensamento lógico ou habilidades técnicas avançadas. Essa visão limitada perpetua estereótipos de gênero e dificulta o desenvolvimento e diversidade no setor, excluindo potenciais talentos por vezes “desperdiçados” em outras áreas.</p>

        <p>No <strong>LearningLab (LL)</strong>, o protagonismo feminino sempre foi de extrema importância — desde o seu início, em 2020, quando o projeto foi fundado por três mulheres: a professora <strong>Jacilane Rabelo</strong> e as alunas <strong>Marina Rocha</strong> e <strong>Gabriela Andrade</strong>. Atualmente, das 15 mulheres do LL, 8 delas (mais da metade) exercem papéis de liderança, o que vai de encontro com o que pode ser observado no mercado de trabalho, onde há uma predominância masculina em papéis de destaque.</p>

        <p>Uma das mulheres que ocupam um papel de liderança, a sub-líder do setor de pesquisas, <strong>Israely Lima</strong>, contou como o projeto foi bastante importante para a sua formação como Engenheira de Software, Analista de Experiência do Usuário e Designer de Interfaces. Acreditando que a participação das mulheres na TI deve ser um tópico trabalhado constantemente, ela destacou as experiências práticas, os projetos com cenários do mercado de trabalho e o incentivo à pesquisa e à docência (principalmente por parte de Jacilane) como essenciais para a sua trajetória.</p>


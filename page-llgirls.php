        <p>Um dos principais objetivos do LLGirls é incentivar a participação feminina em áreas mais relacionadas ao desenvolvimento — problemática que resultou, recentemente, na criação de alguns treinamentos internos visando capacitar mulheres para tais áreas. “É um grande programa de incentivo para trazer mais meninas para o LearningLab, treiná-las e mostrar que elas são capazes de fazer qualquer coisa”, comentou a coordenadora.</p>

        <p>Embora as mulheres sejam as protagonistas de suas trajetórias, o LearningLab entende que lutar pela igualdade de gênero é um dever de todos, e é por isso que os <em>LLBoys</em> também estão bastante engajados nessa tarefa. Para o participante <strong>Eriky Ryan</strong>, um dos que estão à frente desse esforço, o programa LLGirls é importante para a formação de novas líderes femininas dentro do projeto, principalmente para se ter mulheres atuando em áreas onde há atualmente uma predominância de integrantes homens, como desenvolvimento, DevOps, e Análise de Qualidade, etc. “Não só em UX e frontend, que são áreas comumente mais atribuídas a mulheres na tecnologia”, afirmou.</p>

        <p>Jacilane também destacou positivamente essa colaboração entre homens e mulheres. “O legal do LLGirls é que a gente consegue trabalhar junto com os meninos […] O próprio pessoal do projeto começou a ver que existem poucas meninas dentro do setor de desenvolvimento, e os meninos começaram a treinar as meninas para essa área. Eu acho isso um diferencial em um projeto que atende a um público bastante diverso”, comentou a coordenadora.</p>


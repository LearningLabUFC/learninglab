    <p>Entre os artigos de pesquisa apresentados no evento, dois foram projetos do Learning lab,<strong> “Remote Teaching of Scrum in Practice: an analysis of participants' perception “ </strong>e <strong>“Remote Teaching of Requirements Elicitation Techniques in a Pandemic Scenario: A Practical Approach”</strong> projetos nos quais buscaram através da gestão e conhecimento promover estratégias que motivam e mobilizam estudantes a contribuir com sua formação,um dele utilizando o Scrum método usado no primeiro curso realizado pelo projeto, aberto ao público e alunos da ufc, o curso realizado por alunos do projeto tinha como objetivo, ensinar na prática conceitos teóricos de Computação vistos em sala de aula.</p>

    <p>As aulas do curso foram ministradas usando a ferramenta de reuniões online Google Meet, com a frequência dos estudantes sendo computadas via formulário eletrônico, Google Forms, e para a comunicação foi utilizada a ferramenta de comunicação Discord. Os dados coletados das percepções dos estudantes em relação ao ensino utilizando a metodologia Scrum foram analisados qualitativa e quantitativamente </p>

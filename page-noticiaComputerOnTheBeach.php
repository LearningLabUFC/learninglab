    <h1>Campus da Ufc de Russas participa do Computer on The Beach</h2>

    <p>A Universidade do Vale do Itajaí – UNIVALI e sua Escola Politécnica, através dos seus cursos de Ciência da Computação Campus Kobrasol e Campus Itajaí, realizam anualmente o Computer on The Beach.</p>


    <p>O evento técnico-científico visa reunir profissionais, pesquisadores e acadêmicos da área de computação, a fim de discutir as tendências de pesquisa e mercado da computação em suas mais diversas áreas, trazendo um intercâmbio de experiências a todos que participam do projeto.</p>


    <p>Com suas atividades iniciadas de 30 de março até o dia 1 de abril, um dos campus presentes neste evento é o da Universidade Federal (UFC) Campus  Russas , sendo representado pelos docentes: a professora Dra. Patricia Freitas Campos de Vasconcelos, a professora Dra. Jacilane de Holanda Rabelo e por suas discentes egressas do curso de Engenharia de Software, Maria Ellane Mendes Rodrigues e Marina da Silva Rocha, alunas também de mestrado, da UFC de Fortaleza e Quixadá.</p>



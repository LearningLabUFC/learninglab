    <h2>Remote Teaching of Scrum in Practice: an analysis of participants' perception</h2>

    <p>A pandemia trouxe desafios significantes no que se refere a ensinar processos práticos e/ou majoritariamente presenciais num contexto remoto. O ensino das metodologias ágeis, por exemplo, foi um grande desafio enfrentado por profissionais de educação em áreas de TI — entre eles, os que atuam no setor de cursos do LearningLab.</p>

    <p>Esse trabalho esmiuça os desafios, as metodologias, bem como os resultados obtidos a partir da aplicação do curso “Scrum na Prática”, ministrado de forma remota pelo LL para alunos da UFC e da comunidade externa. Os resultados indicaram que a maioria dos estudantes se mostrou satisfeita com o curso e com sua metodologia de ensino.</p>

